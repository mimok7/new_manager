-- 추가된 데이터 확인 (총 20개 행)
SELECT 
  service_type 서비스_타입,
  vehicle_type 차량_타입,
  COUNT(*) as 경로_개수
FROM airport_price
WHERE airport_code LIKE 'AP-%'
GROUP BY service_type, vehicle_type
ORDER BY service_type, vehicle_type;

-- 서비스별 경로 상세 확인
SELECT 
  airport_code,
  service_type,
  vehicle_type,
  route,
  duration
FROM airport_price
WHERE airport_code LIKE 'AP-%' AND airport_code NOT LIKE '%HN%'
ORDER BY 
  CASE WHEN service_type = '픽업' THEN 1 ELSE 2 END,
  CASE WHEN vehicle_type LIKE '%16%' THEN 1 ELSE 2 END,
  route;
