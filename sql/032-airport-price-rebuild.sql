-- 기존 테이블 삭제 (FK 의존성 포함)
DROP TABLE IF EXISTS airport_price CASCADE;

-- ============================================================
-- 새 airport_price 테이블 생성
-- ============================================================
CREATE TABLE airport_price (
  id                    UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  airport_code          TEXT        UNIQUE NOT NULL,        -- 고유 코드 (예: AP-PU-SEDAN-HN)
  service_type          TEXT        NOT NULL,               -- '픽업' / '샌딩'
  vehicle_type          TEXT        NOT NULL,               -- '승용차', 'SUV', '대형 SUV', '9인승 리무진', '11인승 리무진'
  vehicle_examples      TEXT,                               -- 'VIOS, K3 등'
  recommended_capacity  INTEGER,                            -- 권장 승차인원
  max_capacity          INTEGER,                            -- 최대 승차인원
  route                 TEXT        NOT NULL,               -- '하노이 공항 - 하노이 시내'
  route_from            TEXT        NOT NULL DEFAULT '하노이 공항',
  route_to              TEXT        NOT NULL,               -- '하노이 시내', '하이퐁' 등
  duration              TEXT,                               -- '40분', '2시간 30분~3시간' 등
  price                 INTEGER     NOT NULL,               -- 단위: 동(VND)
  year                  INTEGER     DEFAULT 2026,
  is_active             BOOLEAN     DEFAULT true,
  created_at            TIMESTAMPTZ DEFAULT now(),
  updated_at            TIMESTAMPTZ DEFAULT now()
);

-- 인덱스 생성
CREATE INDEX idx_airport_price_service_type    ON airport_price(service_type);
CREATE INDEX idx_airport_price_vehicle_type    ON airport_price(vehicle_type);
CREATE INDEX idx_airport_price_route_to        ON airport_price(route_to);
CREATE INDEX idx_airport_price_year_active     ON airport_price(year, is_active);

