DROP TABLE IF EXISTS rentcar_price CASCADE;

CREATE TABLE rentcar_price (
  id             UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  rent_code      TEXT        UNIQUE,                        -- 식별자 코드 (영문)
  category       TEXT,                                     -- 카테고리 (단독대여 등)
  car_category_code TEXT,                                  -- 차량 카테고리 코드
  vehicle_type   TEXT,                                     -- 차량 종류
  route          TEXT,                                     -- 구간명 전체 표시
  route_from     TEXT,                                     -- 출발지
  route_to       TEXT,                                     -- 목적지
  way_type       TEXT,                                     -- 편도 / 당일왕복 / 다른날왕복 / 시내당일렌트
  price          INTEGER,                                  -- 가격 (VND)
  capacity       INTEGER,                                  -- 최대 탑승인원
  duration_hours INTEGER,                                  -- 이용 시간 (시내당일렌트)
  rental_type    TEXT        DEFAULT '단독대여',
  year           INTEGER     DEFAULT 2026,
  description    TEXT,
  cruise         TEXT        DEFAULT '공통',
  memo           TEXT        DEFAULT '렌트카',
  is_active      BOOLEAN     DEFAULT true,
  created_at     TIMESTAMPTZ DEFAULT now(),
  updated_at     TIMESTAMPTZ DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_rentcar_price_way_type   ON rentcar_price(way_type);
CREATE INDEX IF NOT EXISTS idx_rentcar_price_route_from ON rentcar_price(route_from);
CREATE INDEX IF NOT EXISTS idx_rentcar_price_route_to   ON rentcar_price(route_to);
CREATE INDEX IF NOT EXISTS idx_rentcar_price_year       ON rentcar_price(year);
CREATE INDEX IF NOT EXISTS idx_rentcar_price_vehicle    ON rentcar_price(vehicle_type);

